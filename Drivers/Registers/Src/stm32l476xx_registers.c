#define REG_ENTRY(name, reg) \
    { name, (void*)&(reg), sizeof(reg) }

register_t* get_register(const char* name) {
    if (!name) return NULL;
    for (size_t i = 0; i < REGISTERS_COUNT; i++) {
        if (strcmp(hwregs[i].name, name) == 0) {
            return &hwregs[i];
        }
    }
    return NULL;
}

